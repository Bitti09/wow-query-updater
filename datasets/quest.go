type QuestCategory struct {
	Identifiable
	Category LocalizedField `json:"category"`
	Quests   []Quest        `json:"quests" pg:"-"`
}

type QuestArea struct {
	Identifiable
	Area   LocalizedField `json:"area"`
	Quests []Quest        `json:"quests" pg:"-"`
}

